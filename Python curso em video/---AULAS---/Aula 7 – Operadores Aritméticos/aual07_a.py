n1 = int(input('escreva um valor '))
n2 = int(input('escreva outro valor '))
s = n1+n2
m = n1*n2
d = n1/n2
di = n1//n2
e = n1**n2
print('a soma de {} + {} vale: {}'.format(n1, n2, s))
print('a multiplicação de {} x {} vale: {}'.format(n1, n2, m))
print('a divisão de {} / {} vale: {:.3f}'.format(n1, n2, d))
print('a divisao inteira de {} // {} vale: {}'.format(n1, n2, di))
print('a exponenciação de {} ^ {} vale: {}'.format(n1, n2, e))
